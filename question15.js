// Calculate the slope, x-intercept and y-intercept of y = 2x -2

// Equation: y = 2x - 2

// Slope (m)
const slope = 2;

// Y-intercept (b)
const yIntercept = -2;

// X-intercept
const xIntercept = (0 - yIntercept) / slope;

// Output the results
console.log("Slope:", slope);
console.log("Y-intercept:", yIntercept);
console.log("X-intercept:", xIntercept);
