// Boolean value is either true or false.
//     - Write three JavaScript statement which provide truthy value.
//     - Write three JavaScript statement which provide falsy value.

const a = 5;
const b = 3;
const truthyValue = a > b;
console.log(truthyValue); // Output: true


const truthyValue = "Hello, world!";
console.log(truthyValue); // Output: Hello, world!

const truthyValue = 42;
console.log(truthyValue); // Output: 42

const a = 5;
const b = 8;
const falsyValue = a > b;
console.log(falsyValue); // Output: false

const falsyValue = "";
console.log(falsyValue); // Output: ""

const falsyValue = 0;
console.log(falsyValue); // Output: 0

